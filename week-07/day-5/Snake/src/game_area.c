void draw_game_area()
{
	for (int i = 0; i < 17; ++i) {
		for (int j = 0; j < 30; ++j) {
			if (game_area[i][j] == 1) {
				BSP_LCD_SetTextColor(LCD_COLOR_BLACK);
				BSP_LCD_FillRect(j * 16, i * 16, 16, 16);
			} else if (game_area[i][j] == 2) {
				BSP_LCD_SetTextColor(LCD_COLOR_GREEN);
				BSP_LCD_FillRect(j * 16, i * 16, 16, 16);
			}
		}
	}
}
